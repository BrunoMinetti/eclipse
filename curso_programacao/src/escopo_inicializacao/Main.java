package escopo_inicializacao;

public class Main {

	public static void main(String[] args) {
		// A variável precisa ser inicializada, para não apresentar erro de compilação.  
		
		double price = 400.00;
		double discount;
		
		if (price < 200.00) {
			discount = price * 0.1;
		}
		else {
			discount = 0;
		}
		
		System.out.println(discount);

	}

}
